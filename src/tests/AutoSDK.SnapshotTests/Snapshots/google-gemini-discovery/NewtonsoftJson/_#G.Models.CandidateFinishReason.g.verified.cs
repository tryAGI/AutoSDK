        /// </summary>
        [global::System.Runtime.Serialization.EnumMember(Value="RECITATION")]
        Recitation,
        /// <summary>
        /// The response candidate content was flagged for safety reasons.
        /// </summary>
        [global::System.Runtime.Serialization.EnumMember(Value="SAFETY")]
        Safety,
        /// <summary>
        /// Token generation stopped because the content potentially contains Sensitive Personally Identifiable Information (SPII).
        /// </summary>
        [global::System.Runtime.Serialization.EnumMember(Value="SPII")]
        Spii,
        /// <summary>
        /// Natural stop point of the model or provided stop sequence.
        /// </summary>
        [global::System.Runtime.Serialization.EnumMember(Value="STOP")]
        Stop,
        /// <summary>
        /// Model called too many tools consecutively, thus the system exited execution.
        /// </summary>
        [global::System.Runtime.Serialization.EnumMember(Value="TOO_MANY_TOOL_CALLS")]
        TooManyToolCalls,
        /// <summary>
        /// Model generated a tool call but no tools were enabled in the request.
        /// </summary>
        [global::System.Runtime.Serialization.EnumMember(Value="UNEXPECTED_TOOL_CALL")]
        UnexpectedToolCall,
